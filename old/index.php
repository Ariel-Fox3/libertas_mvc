        b1.attr('disabled', 'disabled');
      },
      success : function(e) {
        var res = JSON.parse(e);
        if (res.erro == false) {
          Swal.fire({
            icon: 'success',
            title: 'Contato enviado',
            text: res.msg
          });
          b1.removeAttr('disabled');
        } else {
          Swal.fire({
            icon: 'error',
            title: 'Erro ao enviar contato',
            text: res.msg
          });
          b1.removeAttr('disabled');
        }
      },
